#dimensions规模，大小
dimensions =(200,50)
#元组用圆括号而不是方括号，其值不可改变
#表格用方括号
print(dimensions[0])
print(dimensions[1])
#for循环遍历元组中所有的值，记得for循环要加冒号
#自动缩进
for dimension in dimensions:
    print (dimension)
score=dimension
#元组赋值
#也可以加元素，,append(0)等语法
print(score)
#元组是比列表更简单的数据元素
#储存的数据不变的话就用元组吧


